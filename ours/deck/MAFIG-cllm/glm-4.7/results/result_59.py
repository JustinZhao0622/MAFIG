def init_planes(nums=5,start_time="8:00:00"):
    """初始化舰载机，每隔三分钟到达一架舰载机，返回舰载机列表，每个舰载机包含时间、id，任务时长都为10分钟"""
    start_time_struct = time.strptime(start_time, "%H:%M:%S")
    base_seconds = time.mktime(start_time_struct)
    planes = []
    accumulated_seconds = 0
    for i in range(nums):
        if i == 0:
            accumulated_seconds = 0
        elif i < 5:
            accumulated_seconds += 3 * 60
        else:
            accumulated_seconds += 8 * 60

        plane_time = time.strftime("%H:%M:%S", time.localtime(base_seconds + accumulated_seconds))
        planes.append({"time": plane_time, "id": i, "duration": 10, "location": (i,10)})
    return planes

