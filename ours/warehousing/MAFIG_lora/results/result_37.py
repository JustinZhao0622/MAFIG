def init_forklifts(nums=3):
    """
    初始化叉车队。
    返回可用叉车列表，每个叉车包含id、坐标
    """
    forklifts = []
    for i in range(nums):
        forklift_id = f"Forklift_{i+1}"
        if forklift_id == "Forklift_3":
            continue
        forklifts.append({
            "id": forklift_id,
            "location": (0, 25),
        })
    forklifts[0]["location"] = (48, 15)
    return forklifts

